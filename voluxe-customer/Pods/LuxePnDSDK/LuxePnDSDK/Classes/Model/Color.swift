import Foundation

@objcMembers public class Color: NSObject, Codable {
    
    dynamic var baseColor: String?
    dynamic var color: String?
    
    convenience init(baseColor: String?, color: String?) {
        self.init()
        self.baseColor = baseColor
        self.color = color
    }
    
}
